// Function to connect to MetaMask
async function connectWallet() {
    if (window.ethereum) {
        provider = new ethers.providers.Web3Provider(window.ethereum);
        try {
           await window.ethereum.request({method: 'eth_requestAccounts'});
           signer = provider.getSigner();
           connectedAccount = await signer.getAddress()
           connectedAddressDisplay.textContent = `Connected: ${connectedAccount}`;
           connectButton.style.display = 'none';
           contract = new ethers.Contract(contractAddress, contractABI, signer);
           loadTokenData();


        } catch (error){
            console.log("Metamask connection failed", error);
            connectedAddressDisplay.textContent = 'Metamask connection failed';
            return
        }
    } else {
       connectedAddressDisplay.textContent = 'Metamask is not installed';
    }
}


